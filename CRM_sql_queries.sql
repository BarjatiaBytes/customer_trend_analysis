SELECT * FROM customer;

-- Q1. What is the total revenue genrated by male vs. female customers?

SELECT gender, SUM(purchase_amount) as revenue
FROM customer
GROUP BY gender;


-- Q2. Which customers used a discount but still spent more than average purchase amount?

SELECT customer_id, purchase_amount
FROM customer 
WHERE discount_applied = 'Yes' AND
purchase_amount > (SELECT AVG(purchase_amount) FROM customer);


-- Q3. Which are the top 5 products with the highest average review rating?
SELECT item_purchased, ROUND(AVG(review_rating::NUMERIC) ,2) AS "Average Product Rating"
FROM customer
GROUP BY item_purchased
ORDER BY AVG(review_rating) DESC
LIMIT 5;


-- Q4. Compare the average Purchase Amounts between Standard and Express Shipping.
SELECT shipping_type,
ROUND(AVG(purchase_amount),2)
FROM customer
WHERE shipping_type IN ('Standard','Express')
GROUP BY shipping_type;


--Q5. Do subscribed customers spend more? Compare average spend and total revenue 
-- between subscribers and non-subscribers.

SELECT subscription_status,
COUNT (customer_id) AS total_customers,
ROUND(AVG(purchase_amount),2) AS avg_spend,
ROUND(SUM (purchase_amount),2) AS total_revenue
FROM customer
GROUP BY 1
ORDER BY 3,2 DESC;


-- Q6. which 5 products have the highest perecentage of purchase with discounts applied?
SELECT item_purchased,
ROUND(100 * SUM( CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END)/ COUNT(*),2) 
AS discount_rate
FROM customer
GROUP BY item_purchased
ORDER BY discount_rate DESC
LIMIT 5;


-- Q7. Segment csutomers into new, returning and loyal customers based on thier 
-- agrregate previous purchases , and show the count of each segement.

with customer_type AS (
SELECT customer_id, previous_purchases,
CASE
	WHEN previous_purchases = 1 THEN 'New'
	WHEN previous_purchases BETWEEN 2 AND 10 THEN 'Returning'
	ELSE 'loyal'
	END AS customer_segment
FROM customer
)
SELECT customer_segment, COUNT(*) AS "Number of Customers"
FROM customer_type
GROUP BY 1
ORDER BY 2 DESC;


-- Q8. What are the top 3 most purchased products within each category?
WITH item_counts AS (
SELECT category,
item_purchased,
COUNT(customer_id) AS total_orders,
ROW_NUMBER() OVER (PARTITION BY category ORDER BY COUNT(customer_id)DESC)
AS item_rank
FROM customer
GROUP BY 1, 2
)
SELECT item_rank, category, item_purchased, total_orders
FROM item_counts
WHERE item_rank <=3;


--Q9. Are customer who are repeat buyers(more than 5 previous purchases) also likely to subscribe?
SELECT subscription_status,
COUNT(customer_id) AS repeat_buyers
FROM customer
WHERE previous_purchases>5
GROUP BY 1;


-- Q.10 What is the revenue contribution of each age group?
SELECT age_group,
SUM(purchase_amount) AS total_revenue
FROM customer
GROUP BY age_group
ORDER BY total_revenue DESC;
